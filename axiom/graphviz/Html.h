#pragma once

#include <string>
#include <string_view>

namespace facebook::axiom::graphviz {

/// Escapes HTML special characters: <, >, &, ".
inline std::string escapeHtml(std::string_view text) {
  std::string result;
  result.reserve(text.size());
  for (char ch : text) {
    switch (ch) {
      case '<':
        result += "&lt;";
        break;
      case '>':
        result += "&gt;";
        break;
      case '&':
        result += "&amp;";
        break;
      case '"':
        result += "&quot;";
        break;
      default:
        result += ch;
    }
  }
  return result;
}

} // namespace facebook::axiom::graphviz
